package uebungen.methoden;

public class Grundrechenarten {


    private double plus(double a, double b) {
        return a + b;
    }
    private double minus(double a, double b) {
        return a - b;
    }
    private double mal(double a, double b) {
        return a * b;
    }
    private double geteilt(double a, double b) {
        return a/b;
    }
}
